# 受信した64ビットの符号語
received_code = '11001101 11000011 11000110 11101011 11011000 11101000 11110011 10100000'

# 8ビットごとに分割
codes = received_code.split()

# ASCIIコード表
ascii_table = {
    '1000001': 'A',
    '1000010': 'B',
    '1000011': 'C',
    '1000100': 'D',
    '1000101': 'E',
    '1000110': 'F',
    '1000111': 'G',
    '1001000': 'H',
    '1001001': 'I',
    '1001010': 'J',
    '1001011': 'K',
    '1001100': 'L',
    '1001101': 'M',
    '1001110': 'N',
    '1001111': 'O',
    '1010000': 'P',
    '1010001': 'Q',
    '1010010': 'R',
    '1010011': 'S',
    '1010100': 'T',
    '1010101': 'U',
    '1010110': 'V',
    '1010111': 'W',
    '1011000': 'X',
    '1011001': 'Y',
    '1011010': 'Z',
    # 他の文字も追加...
}

# パリティビットを除いてASCII文字に変換
message = ''
for code in codes:
    ascii_code = code[:-1]  # 最後のビット（パリティビット）を除く
    if ascii_code in ascii_table:
        message += ascii_table[ascii_code]
    else:
        message += '?'  # 不明なコードは'?'に置き換える

print(message)
